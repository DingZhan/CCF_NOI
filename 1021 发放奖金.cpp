#include <iostream>
#include <iomanip>
using namespace std;

int main()
{
    double x, y;
    cin>>x;
    if(x<=100000)
        y = x*0.10;
    else if(x<200000)
        y = 100000*0.10 + (x-100000)*0.075;
    else if(x<400000)
        y = 100000*0.10 + (200000-100000)*0.075 + (x-200000)*0.05;
    else if(x<600000)
        y = 100000*0.10 + (200000-100000)*0.075 + (400000-200000)*0.05 + (x-400000)*0.03;
    else if(x<1000000)
        y = 100000*0.10 + (200000-100000)*0.075 + (400000-200000)*0.05 + (600000-400000)*0.03+(x-600000)*0.015;
    else
        y = 100000*0.10 + (200000-100000)*0.075 + (400000-200000)*0.05 + (600000-400000)*0.03+(1000000-600000)*0.015+(x-1000000)*0.01;
    cout<<fixed<<setprecision(3)<<y<<endl;

    return 0;
}
